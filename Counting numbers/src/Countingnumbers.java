public class Countingnumbers {
    public static void main(String[] args) {
        for (int i = 200000; i <= 210000; i++) {
            System.out.println(i);
        }
        int i = 220000;
        while (i <= 235000) {
            System.out.println(i);
            i = i + 1;
        }
    }
}
